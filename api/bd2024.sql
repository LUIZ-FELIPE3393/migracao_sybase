-- tabelas
CREATE TABLE estadocivil (
  estcodigo int NOT NULL,
  estdescricao varchar(40) NOT NULL,
  PRIMARY KEY (estcodigo)
);

CREATE TABLE cidade (
  cidcodigo int DEFAULT 0,
  cidnome varchar(80) NOT NULL,
  PRIMARY KEY (cidcodigo)
);

CREATE TABLE zona (
  zoncodigo int DEFAULT 0,
  zonnome varchar(15) NOT NULL,
  zoncidcodigo int NOT NULL REFERENCES cidade (cidcodigo),
  PRIMARY KEY (zoncodigo),
);

CREATE TABLE bairro (
  baicodigo int DEFAULT 0,
  bainome varchar(30) NOT NULL,
  baizoncodigo int NOT NULL REFERENCES zona(zoncodigo),
  baiqtdepessoas unsigned int DEFAULT 0,
  PRIMARY KEY (baicodigo)
);

CREATE TABLE fornecedor (
  forcnpj char(18) DEFAULT '',
  fornome varchar(100) NOT NULL,
  forfone char(9) NOT NULL,
  forcidcodigo int NOT NULL REFERENCES cidade (cidcodigo),
  PRIMARY KEY (forcnpj)
);

CREATE TABLE fornecedorfone (
  ffforcnpj char(18) NOT NULL REFERENCES fornecedor (forcnpj),
  fffone char(9) NOT NULL,
  PRIMARY KEY (ffforcnpj,fffone)
);

CREATE TABLE funcionario (
  funcodigo int DEFAULT 0,
  funnome varchar(50) NOT NULL,
  funsalario numeric(6,2) NOT NULL,
  funbaicodigo int NOT NULL REFERENCES bairro (baicodigo),
  funcodgerente int NULL REFERENCES funcionario (funcodigo),
  fundtdem date NULL,
  funestcodigo int NOT NULL REFERENCES estadocivil (estcodigo),
  funsenha varchar(20) NULL,
  funlogin varchar(30) NULL,
  PRIMARY KEY (funcodigo)
);

CREATE TABLE filial (
  filcodigo int DEFAULT 0,
  filnome varchar(40) NOT NULL,
  filcodgerente int NOT NULL REFERENCES funcionario (funcodigo),
  filbaicodigo int NOT NULL REFERENCES bairro (baicodigo),
  PRIMARY KEY (filcodigo),
);

CREATE TABLE grupoproduto (
  grpcodigo int NOT NULL,
  grpdescricao varchar(40) NOT NULL,
  grpcomissao numeric(4,2) NOT NULL,
  grpativo char(1) NOT NULL,
  PRIMARY KEY (grpcodigo)
);

CREATE TABLE produto (
  procodigo int DEFAULT 0,
  pronome varchar(80) NOT NULL,
  procusto numeric(7,2) NOT NULL,
  propreco numeric(7,2) NOT NULL,
  proativo char(1) NOT NULL,
  progrpcodigo int NOT NULL REFERENCES grupoproduto (grpcodigo),
  prosaldo int NOT NULL,
  proforcnpj char(18) NOT NULL REFERENCES fornecedor (forcnpj),
  PRIMARY KEY (procodigo)
);

CREATE TABLE cliente (
  clicodigo int IDENTITY,
  clisexo char(1) NOT NULL,
  clirendamensal numeric(6,2) NOT NULL,
  clinome varchar(60) NOT NULL,
  clibaicodigo int NOT NULL REFERENCES bairro (baicodigo),
  clifone varchar(10) DEFAULT '',
  cliestcodigo int NOT NULL REFERENCES estadocivil (estcodigo),
  clidtcadastro date NULL,
  clidtdesativacao date NULL,
  PRIMARY KEY (clicodigo),
);

CREATE TABLE clientestatus (
    csclicodigo INT NOT NULL,
    csstatus VARCHAR(50) NOT NULL,
    PRIMARY KEY (csclicodigo),
    FOREIGN KEY (csclicodigo) REFERENCES cliente (clicodigo)
);

CREATE TABLE venda (
  vencodigo int NOT NULL,
  vendata date NOT NULL,
  venfilcodigo int NOT NULL REFERENCES filial (filcodigo),
  venclicodigo int NOT NULL REFERENCES cliente (clicodigo),
  venfuncodigo int NOT NULL REFERENCES funcionario (funcodigo),
  PRIMARY KEY (vencodigo)
);

CREATE TABLE itemvenda (
  itvvencodigo int NOT NULL REFERENCES venda (vencodigo),
  itvprocodigo int NOT NULL REFERENCES produto (procodigo),
  itvqtde int NOT NULL,
  PRIMARY KEY (itvvencodigo,itvprocodigo),
);

